    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        // TODO add your handling code here:
        JFileChooser browseEmployeeImage = new JFileChooser();

        //Filter image extensions
        FileNameExtensionFilter fnef = new FileNameExtensionFilter("IMAGES", "png", "jpg", "jpeg");
        browseEmployeeImage.addChoosableFileFilter(fnef);
        int showOpenDialogue = browseEmployeeImage.showOpenDialog(null);
        if (showOpenDialogue == JFileChooser.APPROVE_OPTION) {
            File selectedImageFile = browseEmployeeImage.getSelectedFile();
            String selectedImagePath = selectedImageFile.getAbsolutePath();
            
            txtImageLocation.setText(selectedImagePath);
            //Display image in JLable
            ImageIcon Ii = new ImageIcon(selectedImagePath);
            //Resize image to fit jlabel
            Image image = Ii.getImage().getScaledInstance(lblEmployeePicture.getWidth(), lblEmployeePicture.getHeight(), Image.SCALE_SMOOTH);
            lblEmployeePicture.setIcon(new ImageIcon(image));
        }
    }//GEN-LAST:event_btnUploadActionPerformed


