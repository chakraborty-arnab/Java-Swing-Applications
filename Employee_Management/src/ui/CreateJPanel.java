    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try{
            String employeeId = "";
            String age = "";
            String name = txtName.getText();
            try{

            employeeId = txtEmployeeId.getText();
            age = txtAge.getText() ;
            }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please enter valid values.");
            return;
        }
        String gender = txtGender.getText();
        String startDate = txtStartDate.getText();
        String level = txtLevel.getText();
        String teamInfo = txtTeamInfo.getText();
        String positionTitle = txtPositionTitle.getText();
        String phoneNumber = txtPhoneNumber.getText() ;
        String emailId = txtEmailId.getText();
        String imageLocation = txtImageLocation.getText();
        
        Employee e = new Employee();

        if(txtName.getText().isEmpty() || txtEmployeeId.getText().isEmpty()||txtAge.getText().isEmpty()
           || txtGender.getText().isEmpty() || txtStartDate.getText().isEmpty() || txtLevel.getText().isEmpty()
            || txtTeamInfo.getText().isEmpty() || txtPositionTitle.getText().isEmpty() || txtPhoneNumber.getText().isEmpty()
             || txtEmailId.getText().isEmpty() || txtImageLocation.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(this, "Please Enter All Details");
            return;
        }    
        
        else if(!name.matches("^[a-zA-Z]+(([',. -][a-zA-Z ])?[a-zA-Z]*)*$")){ 
            JOptionPane.showMessageDialog(this, "Invalid Name");
            return;
        }
        else if(  Integer.parseInt(employeeId)   <0 ||Integer.parseInt(employeeId) >99999){
            JOptionPane.showMessageDialog(this, "Invalid Employee Id; Cannot Exceed 99999");
            return;
            } 
        else if(Integer.parseInt(age) <0 ||(Integer.parseInt(age))>100){
            JOptionPane.showMessageDialog(this, "Invalid age; Limit 0-100");
            return;
        }
        else if(!gender.equalsIgnoreCase("male") && !gender.equalsIgnoreCase("female") && !gender.equalsIgnoreCase("other")){
        JOptionPane.showMessageDialog(this, "Please Enter Gender As: Male/Female/Other");
        return;
        }
        else if(!startDate.matches("^(?:(?:(?:0?[13578]|1[02])(\\/|-|\\.)31)\\1|" +
            "(?:(?:0?[1,3-9]|1[0-2])(\\/|-|\\.)(?:29|30)\\2))" +
            "(?:(?:1[6-9]|[2-9]\\d)?\\d{2})$|^(?:0?2(\\/|-|\\.)29\\3" +
            "(?:(?:(?:1[6-9]|[2-9]\\d)?(?:0[48]|[2468][048]|" +
            "[13579][26])|(?:(?:16|[2468][048]|[3579][26])00))))$|" +
            "^(?:(?:0?[1-9])|(?:1[0-2]))(\\/|-|\\.)(?:0?[1-9]|1\\d|" +
            "2[0-8])\\4(?:(?:1[6-9]|[2-9]\\d)?\\d{2})$")){
        JOptionPane.showMessageDialog(this, "Invalid Start Date.");
        return;
        }
        else if(level.isEmpty()){
        JOptionPane.showMessageDialog(this, "Invalid Level.");
        return;
        }
        else if(teamInfo.isEmpty()){
        JOptionPane.showMessageDialog(this, "Invalid Team Information.");
        return;
        }
        else if(positionTitle.isEmpty()){
        JOptionPane.showMessageDialog(this, "Invalid Position Title.");
        return;
        }
        else if(!phoneNumber.matches("^\\D?(\\d{3})\\D?\\D?(\\d{3})\\D?(\\d{4})$")){
        JOptionPane.showMessageDialog(this, "Invalid Phone Number.");
        return;
        }
        else if(!emailId.matches("^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*"+"@" + "(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$")){
        JOptionPane.showMessageDialog(this, "Invalid Email Id.");
        return;
        }
        else if(!imageLocation.matches("[^\\s]+(.*?)\\.(jpg|jpeg|png|gif|JPG|JPEG|PNG|GIF)$")){
        JOptionPane.showMessageDialog(this, "Invalid Image Location");
        return;
        }
        else{    
            e.setName(name);
            e.setEmployeeId(Integer.parseInt(employeeId));
            e.setAge(Integer.parseInt(age));
            e.setGender(gender);
            e.setStartDate(startDate);
            e.setLevel(level);
            e.setTeamInfo(teamInfo);
            e.setPositionTitle(positionTitle);
            e.setPhoneNumber(phoneNumber);
            e.setEmailId(emailId);
            e.setImageLocation(imageLocation);
            
            this.list.addNewEmployee(e);

            JOptionPane.showMessageDialog(this,"Person Info Saved");

            txtName.setText("");
            txtEmployeeId.setText("");
            txtAge.setText("");
            txtGender.setText("");
            txtStartDate.setText("");
            txtLevel.setText("");
            txtTeamInfo.setText("");
            txtPositionTitle.setText("");
            txtPhoneNumber.setText("");
            txtEmailId.setText("");
            txtImageLocation.setText("");
            lblEmployeePicture.setIcon(null);
        }
        }
        catch(Exception e){
                JOptionPane.showMessageDialog(this, "Error, Please Try Again.");
                }
    }//GEN-LAST:event_btnSaveActionPerformed

